package mycom.stream.groupby;

public class NetworkLimitParams {
    private String serverInstanceCode;
    private String serverIp;
    private String serverInstanceIp;

    public String getServerInstanceCode() {
        return serverInstanceCode;
    }

    public void setServerInstanceCode(String serverInstanceCode) {
        this.serverInstanceCode = serverInstanceCode;
    }

    public String getServerIp() {
        return serverIp;
    }

    public void setServerIp(String serverIp) {
        this.serverIp = serverIp;
    }

    public String getServerInstanceIp() {
        return serverInstanceIp;
    }

    public void setServerInstanceIp(String serverInstanceIp) {
        this.serverInstanceIp = serverInstanceIp;
    }
}
